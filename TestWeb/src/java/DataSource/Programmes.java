package DataSource;

/**
 *
 * @author CS_admin
 */
public class Programmes
  {
    private String name;
    private String title;
    private String aps;
    private String institution;
    private String subjects;
    private String duration;
    private String carees;
    private String field;
    private String status;
    private String extraMaterial;
    private String studyTips;
    private String news;
   

    public Programmes()
      {
      }

    public String getName()
      {
        return name;
      }

    public String getStatus()
      {
        return status;
      }

    public void setStatus(String status)
      {
        this.status = status;
      }

    public void setName(String name)
      {
        this.name = name;
      }

    public String getTitle()
      {
        return title;
      }

    public void setTitle(String title)
      {
        this.title = title;
      }

    public String getAps()
      {
        return aps;
      }

    public void setAps(String aps)
      {
        this.aps = aps;
      }

    public String getInstitution()
      {
        return institution;
      }

    public void setInstitution(String institution)
      {
        this.institution = institution;
      }

    public String getSubjects()
      {
        return subjects;
      }

    public void setSubjects(String subjects)
      {
        this.subjects = subjects;
      }

    public String getDuration()
      {
        return duration;
      }

    public void setDuration(String duration)
      {
        this.duration = duration;
      }

    public String getCarees()
      {
        return carees;
      }

    public void setCarees(String carees)
      {
        this.carees = carees;
      }

    public String getField()
      {
        return field;
      }

    public void setField(String field)
      {
        this.field = field;
      }

    public String getExtraMaterial()
      {
        return extraMaterial;
      }

    public void setExtraMaterial(String extraMaterial)
      {
        this.extraMaterial = extraMaterial;
      }

    public String getStudyTips()
      {
        return studyTips;
      }

    public void setStudyTips(String studyTips)
      {
        this.studyTips = studyTips;
      }

    public String getNews()
      {
        return news;
      }

    public void setNews(String news)
      {
        this.news = news;
      }
    
  }
